package CentricToAll3;

public class Lab075 {
	public static void main(String[] args)
	{
		
		//Printing of even numbers in ascending order
		
		int i=0;
		while(i<=10)
		{
			System.out.println(i);
			i=i+2;
		}
		
		
		
		//Printing of odd numbers in ascending order
		
		int i1=1;
		while(i1<=10)
		{
			System.out.println(i1);
			i1=i1+2;
		}
	}

}
