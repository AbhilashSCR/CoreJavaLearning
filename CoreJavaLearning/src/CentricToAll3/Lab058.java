package CentricToAll3;

public class Lab058 {
	public static void main(String[] args)
	{
		
		//Printing the even numbers using for loop
		
		for(int i=1; i<=50;i++)
		{
			if (i%2==0)
			{
				System.out.println("Even number is: " + i);
			}
		}
		
		
		System.out.println("Next code is shown below");
		//Reverse order/Decrementing
		
		for(int i=50; i>=1;i--)
		{
			if(i%2==0)
			{
				System.out.println("Even number is: "+i);
			}
		}

}
}