package CentricToAll3;

public class Lab076 {

	public static void main(String[] args)
	{
		
		
		//Printing of even numbers in descending order 
		int i=10;
		while(i>=1)
		{
			System.out.println(i);
			i=i-2;
		}
		
		
		//Printing of odd numbers in descending order
		
		int i1=11;
		while(i1>=1)
		{
			System.out.println(i1);
			i1=i1-2;
		}
	}
}
