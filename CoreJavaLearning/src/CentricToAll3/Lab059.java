package CentricToAll3;

public class Lab059 {
 public static void main(String[] args)
 {
	 //Printing odd number using for loop
	 
	 for(int i=1;i<=49;i++)
	 {
		 if(i%2==1)
		 {
			 System.out.println("Odd number is:" + i);
		 }
	 }
	 
	 System.out.println("Next code is shown below");
	 //Reverse order
	 
	 for(int i=43; i>=1;i--)
	 {
		 if(i%2==1)
		 {
			 System.out.println("Odd number is:" +i);
		 }
	 }
	 
 }
}
