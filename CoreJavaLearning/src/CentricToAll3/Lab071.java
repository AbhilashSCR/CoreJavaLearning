package CentricToAll3;

public class Lab071 {
	
	public static void main(String[] args)
	{
		//While loop is used to execute a piece of code repeatedly.
		//When the number of iterations are not fixed.
		//It is also known as entry controlled loop
		
		
		int i=1;
		while(i<=10)
		{
			System.out.println(i);
			i++;   //If we don't provide the increment/decrement condition,it keeps printing
		}
		
	}

}
