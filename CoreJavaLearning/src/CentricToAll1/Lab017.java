package CentricToAll1;

public class Lab017 {

	public static void main(String[] args)
	{
	
		//Shift operators: 
		
		//Left shift operator << is used for Multiplication 
		int a =56;
		int b=64;
		System.out.println(a<<1);  //56*1
		System.out.println(b<<3);  //64*8
		
		//Right shift operator is used for division
		
		System.out.println(a>>4); //56/16
		System.out.println(b>>5); //64/32
		
		
		
		
	}
}
